package NSDLTraining.CalculatorAPI;

public class CalculatorAPI 
{

	public int Addition(int num1,int num2)
	{
		
		int Res;
		Res=num1+num2;
		return Res;
	}

	public int Subtraction(int num1,int num2)
	{
		
		int Res;
		Res=num1-num2;
		return Res;
	}

	public int Multiplication(int num1,int num2)
	{
		
		int Res;
		Res=num1*num2;
		return Res;
	}
}
